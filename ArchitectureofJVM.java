package anudip;

public class ArchitectureofJVM {

	public static void main(String[] args)  {
		System.out.println("The JVM is a program that runs Java bytecode.");
	    System.out.println("It's the runtime environment for Java programs.");
	    System.out.println("The JVM has three main parts: Class Loader, Runtime Data Areas, and Execution Engine.");
	    System.out.println("The Class Loader loads Java classes into memory.");
	    System.out.println("The Runtime Data Areas provide memory spaces for Java programs.");
	    System.out.println("The Execution Engine executes Java bytecode.");
	    System.out.println("The JVM provides a safe environment for Java programs to run in.");
	    System.out.println("It loads, links, and initializes classes.");
	    System.out.println("The JVM checks for errors during runtime.");
	    System.out.println("It has a garbage collector to manage memory.");
	    System.out.println("The JVM uses a stack-based architecture.");
	    System.out.println("It has registers to store and manipulate data.");
	    System.out.println("The JVM stores class metadata and method bytecode in a method area.");
	    System.out.println("It stores objects and their data in a heap area.");
	    System.out.println("The JVM interacts with native code and libraries through a native method interface.");
	  }

	}


